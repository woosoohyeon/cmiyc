<template>
  <div class="map">
    <b-navbar type="dark" variant="dark">
      <b-collapse is-nav>
        <router-link to="/">
        <b-navbar-brand>Home</b-navbar-brand>
        </router-link>
      </b-collapse>
      <b-navbar-nav align="right">
        <b-button v-b-toggle.sidebar-1>Top3</b-button>
      </b-navbar-nav>
    </b-navbar>
    
    <b-sidebar id="sidebar-1" title="Sidebar" shadow>
      <div class="px-3 py-2">
        {{test}}
        <b-img src="https://picsum.photos/500/500/?image=54" fluid thumbnail></b-img>
      </div>
    </b-sidebar>
    <naver-maps
      :height="height"
      :width="width"
      :mapOptions="mapOptions"
      :initLayers="initLayers">
      <naver-marker v-for="(item, idx) in mask" :key="idx" :lat="item.lat" :lng="item.lng" @click="onMarkerClicked(idx)"></naver-marker>
      <!-- 위치제공 동의를 하지 않으면 현재위치를 마커로 표시하지 않습니다. -->
      <naver-marker v-if="nowLocate.lat!==0" :lat="nowLocate.lat" :lng="nowLocate.lng"></naver-marker>
      <naver-info-window
        @load="onInfoLoad"
        :isOpen="info"
        :marker="marker">
        <div>
          <b-card
            img-src="https://picsum.photos/600/300/?image=25"
            img-alt="Image"
            img-top
            style="max-width: 20rem;"
            class="mb-2"
          >
            <h5 style="font-weight: bold;">{{infoWindow.name}}</h5>
            <b-card-text>
              Some quick example text to build on the card title and make up the bulk of the card's content.
            </b-card-text>
            <b-button href="#" variant="primary">Go somewhere</b-button>
          </b-card>
        </div>
      </naver-info-window>
    </naver-maps>
  </div>
</template>

<script>
import maskData from '../assets/maskDATA.json'

export default {
  name: 'Map',
  created(){
    // Map화면으로 들어오면 항상 새롭게 현재 위치를 물어봅니다
    navigator.geolocation.getCurrentPosition(
      (pos)=>{
          this.mapOptions.lat = pos.coords.latitude,
          this.mapOptions.lng = pos.coords.longitude
      },
      (err)=>console.err(err)
    )
  },
  data(){
    return{
      width: screen.availWidth,
      height: screen.availHeight,
      info: false,
      infoWindow:{
        name: null,
      },
      marker: null,
      mapOptions: {
        lat: 37.224944,
        lng: 127.183181,
        zoom: 14,
        minZoom: 14,
        zoomControlOptions: {position: 'TOP_RIGHT'},
      },
        initLayers: ['BACKGROUND', 'BACKGROUND_DETAIL', 'POI_KOREAN']
      ,
      nowLocate :{
        lat: 0,
        lng: 0
      },
      mask:[],
      }
    },
  methods: {
    onMarkerClicked(idx) {
      this.marker = this.mask[idx]; // 현재 마커 할당
      this.info = !this.info; // 인포 윈도우 표시
      this.infoWindow.name = this.mask[idx].name;
    },
  },
  mounted(){
    maskData.storeInfos.forEach(element => {
      this.mask.push(element)
    });
  }
}
</script>
